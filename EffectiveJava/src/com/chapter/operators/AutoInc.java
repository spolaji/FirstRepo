package com.chapter.operators;

import static com.chapter.everythingisanobject.Print.*;

public class AutoInc {
	public static void main(String[] args) {
		int i = 1;
		println("i: " + i);
		println("++i: " + ++i);
		println("i++: " + i++);
		println("i: " + i);
		println("--i: " + --i);
		println("i--: " + i--);
		println("i: " + i);
		
	}
}
